    private  boolean validarHora(String cad) {
        String patron = "^([01]?[0-9]|2[0-3]):[0-5][0-9]$";
        Pattern pattern = Pattern.compile(patron);
        Matcher matcher = pattern.matcher(cad);
        return matcher.matches();
    }
